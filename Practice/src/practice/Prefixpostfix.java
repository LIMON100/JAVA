
package practice;

public class Prefixpostfix {
    public static void main(String[] args) {
        int x=10;
        int a,b;
        a=++x;
        System.out.println(a);       
        b=x++;
        System.out.println(b);
        a=x--;
        System.out.println(a);
        b=--x;
        System.out.println(b);
    }
}
